void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM1) 
  {
    time_counter++;

    if (led_state == 1) 
    { // LED is ON
      if (time_counter >= target_count) 
      {
        HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET); // Turn OFF LED
        led_state = 0;
        time_counter = 0;
        __disable_irq();
        if (led_mode == 0) {
          current_n++;
        } else if (led_mode == 1) {
          if(current_n > 1) current_n--;
          else {
            current_n = 1;
          } // 立即进行边界检查
        } else if (led_mode == 2) {
          current_n = 5;
        }
        __enable_irq();

        target_count = current_n; // 更新灭灯时间目标
      }
    } 
    else 
    { // LED is OFF
      if (time_counter >= target_count) 
      { // 修正：统一使用target_count判断
        HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET); // Turn ON LED
        led_state = 1;
        time_counter = 0;
        target_count = 5; // 亮灯时间固定
      }
    }
  }
}
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == GPIO_PIN_3) {
    // 切换到递减模式
    __disable_irq();
    led_mode = 1;
    __enable_irq();
  } else if (GPIO_Pin == GPIO_PIN_4) {
    // 切换到固定间隔模式
    __disable_irq();
    led_mode = 2;
    __enable_irq();
  }
}
